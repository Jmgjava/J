package org.java.var2;

public class VarEx4 {

	public static void main(String[] args) {
		System.out.println("변수");
		System.out.println("기본자료형(Primitive Type)");
		
		// 1.변수선언, 2. 10을 변수 b에 초기화한다
		byte b=10;  // 1byte -2^7~+2^7-1  -128~+127 (256)
		short s=10; // 2byte -2^15~+2^15-1 
		int i=10;   // 4byte -2^31~+2^31-1
		long l=10;  // 8byte -2^63~+2^63-1 
		float f=1.1f;  // (float)1.1  -> 형변환(타입변환)
		double d=1.1;  
		char ch='a';
		boolean bool=false;
		
		
	}
}
